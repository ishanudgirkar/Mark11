const dateOfBirth = document.querySelector("#date-of-birth");
const luckyNumber = document.querySelector("#lucky-number");
const checkNumberButton = document.querySelector("#check-number");
const outputBox = document.querySelector("#output-box");

function checkBirthDateIsLucky() {
  const dob = dateOfBirth.value;
  const sum = calculateSum(dob);
  if (sum && dob)
    if (Number(luckyNumber.value) < 0 || Number(luckyNumber.value) === 0) {
      outputBox.innerText = "Lucky number can't be negative OR zero ! ðŸ˜“"
    } else {
      compareValues(sum, luckyNumber.value);
    }
  else
    outputBox.innerText = "Please enter both the fields ðŸ˜ ";
}

function compareValues(sum, luckyNumber) {
  if (sum % luckyNumber === 0) {
    outputBox.innerText = "Your birthday is lucky ðŸ¤ž ";
  } else {
    outputBox.innerText = "Your birthday is not lucky ðŸ˜” ";
  }
}





function calculateSum(dob) {
  dob = dob.replaceAll("-", "");
  let sum = 0;
  for (let index = 0; index < dob.length; index++) {
    sum = sum + Number(dob.charAt(index));
  }
  return sum;
}



